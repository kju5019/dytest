package ch06;

public class Customer {
	String id;
	String password;
	String name;
	String tel;
	String addr;
	
	Customer(){}
	Customer(String id, String password, String name, String tel, String addr) {
		this.id = id;
		this.password = password;
		this.name = name;
		this.tel = tel;
		this.addr = addr;
	}

}
